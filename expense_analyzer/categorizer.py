def _matches(kw: str, desc_lower: str) -> bool:
    """
    ASCII のみのキーワード（略語・サービス名）は単語境界でマッチ。
    日本語キーワードは部分一致。
    """
    kw_lower = kw.lower()
    if re.match(r'^[a-z0-9+&#!]+$', kw_lower):
        # 前後が英数字でないことを確認（例: "etf" が "netflix" にマッチしない）
        return bool(re.search(
            r'(?<![a-z0-9])' + re.escape(kw_lower) + r'(?![a-z0-9])',
            desc_lower,
        ))
    return kw_lower in desc_lower


